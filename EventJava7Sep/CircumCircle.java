class CircumCircle{
    public static void main(String[] args) {
        
        final double pi = 3.14;
        double radius = 5.5;
        double circum;

        circum = 2 * pi * radius;

        System.out.println("pi value is : " + pi);
        System.out.println("Radius value is "+ radius);
       System.out.println("Circumference is : "+ circum);
    }
}